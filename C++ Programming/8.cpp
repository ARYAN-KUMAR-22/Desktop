#include <iostream>
using namespace std;

int main()
{
	// ADDING NUMBERS AND STRINGS 
	
	int x = 10;
	int y = 20;
	
	int z = x + y; // Z WILL BE 30 ( AN INTEGER )

	cout << z <<"\n";

	string a = "10";
	string b = "20";

	string c = a + b; // C WILL BE 1020 ( A STRING )

	cout << c;
	// IF YOU TRY TO ADD A STRING TO A NUMBER IT WILL THROUGH ERROR.
	return 0;

}
